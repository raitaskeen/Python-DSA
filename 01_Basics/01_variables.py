# Variables in Python

# What is a Variable?
#A variable is like a box where you can store a value. You can give this box a name,
#and then you can use the name to get the value from the box whenever you need it.

# 
# How to create a variable?
# In Python, you can create a variable by giving it a name and assigning a value to it.
# The assignment operator in Python is the equal sign (=).

# Storing a number
age = 10
print("Age:", age)

# Storing a word
name = "Alice"
print("Name:", name)

# Storing a decimal number
height = 4.5
print("Height:", height)

# Storing a true/false value
is_student = True
print("Is student:", is_student)


# Rectangle Area and Perimeter Calculator

# Ask the user for the length of the rectangle
length = float(input("Enter the length of the rectangle: "))

# Ask the user for the width of the rectangle
width = float(input("Enter the width of the rectangle: "))

# Calculate the area of the rectangle
area = length * width

# Calculate the perimeter of the rectangle
perimeter = 2 * (length + width)

# Print the results
print("The area of the rectangle is:", area)
print("The perimeter of the rectangle is:", perimeter)