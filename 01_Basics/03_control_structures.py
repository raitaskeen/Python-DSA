# Control Structures in Python

# If-Else Statement Example
age = int(input("Enter your age: "))
if age < 13:
    print("You are a child.")
elif age < 20:
    print("You are a teenager.")
else:
    print("You are an adult.")

# For Loop Example
for i in range(1, 6):
    print(i)

# While Loop Example
count = 1
while count <= 5:
    print(count)
    count += 1