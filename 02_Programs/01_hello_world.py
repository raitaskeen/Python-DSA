# Enhanced Interactive Hello, World! Program

# Ask the user for their name
# The input() function is used to take input from the user.
# The user's input is stored in the variable 'name'.
name = input("Enter your name: ")

# Print a personalized greeting
# The f-string is used to include the user's name in the greeting.
print(f"Hello, {name}! Welcome to the world of Python programming.")

# Additional fun: Ask the user how they are feeling
# The input() function is used again to take input from the user.
# The user's input is stored in the variable 'feeling'.
feeling = input("How are you feeling today? ")

# Respond based on the user's input
# The f-string is used to include the user's name and feeling in the response.
print(f"That's great to hear that you're feeling {feeling}, {name}!")

# Ask the user for their favorite color
# The input() function is used to take input from the user.
# The user's input is stored in the variable 'favorite_color'.
favorite_color = input("What's your favorite color? ")

# Respond based on the user's input
# The f-string is used to include the user's favorite color in the response.
print(f"{favorite_color} is a beautiful color!")

# Ask the user for their favorite hobby
# The input() function is used to take input from the user.
# The user's input is stored in the variable 'hobby'.
hobby = input("What's your favorite hobby? ")

# Respond based on the user's input
# The f-string is used to include the user's hobby and name in the response.
print(f"That sounds fun! I hope you get to enjoy {hobby} often, {name}!")

# Final message
# The f-string is used to include the user's name in the final message.
print(f"It was nice chatting with you, {name}. Have a great day!")